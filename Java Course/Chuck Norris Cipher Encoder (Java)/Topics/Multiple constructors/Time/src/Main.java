class Time {

    int hours;
    int minutes;
    int seconds;

    public Time (int hours){
        this.hours = hours;
    }
    public Time (int hours, int minute){
        this.hours = hours;
        this.minutes = minute;
    }
    public Time (int hours, int minute, int seconds){
        this.hours = hours;
        this.minutes = minute;
        this.seconds = seconds;
    }
}